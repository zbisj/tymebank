// 1.1. EXTERNAL DEPENDENCIES ..................................................

import React from "react";
import PropTypes from "prop-types";

// 1.1. END ....................................................................

// 1.2. INTERNAL DEPENDENCIES ..................................................
import { ButtonIcon, ButtonContainer } from "./index.styles";
// 1.2. END ....................................................................

// 1.3. IMAGES .................................................................
// 1.3. END ....................................................................

// 1.4. DATA ...................................................................
// 1.4. END ....................................................................

// 1.5. GLOBALS ................................................................
// 1.5. END ....................................................................

// 1.5. COMPONENT ..............................................................

const Buton = ({ size, type, text, iconRight, iconLeft }) => {
  // 1.5.1. HOOKS & API CALLS ..................................................
  // 1.5.1. END ................................................................

  // 1.5.2. FUNCTIONS & LOCAL VARIABLES
  const renderPosition = () => {
    if (iconLeft) return "left";
    return "right";
  };
  // 1.5.2. END

  // 1.5.3. RENDER

  return (
    <ButtonContainer size={size} type={type}>
      {iconLeft && (
        <ButtonIcon $position={renderPosition()}>{iconLeft}</ButtonIcon>
      )}
      <span>{text}</span>
      {iconRight && (
        <ButtonIcon $position={renderPosition()}>{iconRight}</ButtonIcon>
      )}
    </ButtonContainer>
  );

  // 1.5.3. RENDER
};

// 1.5. END ....................................................................

// 1.6. STYLES .................................................................
// 1.6. END ....................................................................

// 1.7. PROPS ..................................................................

// 1.7.1. REDUX ................................................................

// 1.7.1. END ..................................................................

// 1.7.2. PROP TYPES

Buton.propTypes = {
  text: PropTypes.string.isRequired,
  size: PropTypes.oneOf(["small", "medium", "large"]),
  type: PropTypes.string,
  iconLeft: PropTypes.node,
  iconRight: PropTypes.node,
};

// 1.7.2. END

// 1.7. END ....................................................................

export default Buton;

