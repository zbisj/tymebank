export const addColor = (color) => {
  switch (color) {
    case "grey":
      return colors.grey;
    case "black":
      return colors.black;
    case "primary":
      return colors.primary;
    case "lightGrey":
      return colors.lightGrey;
    default:
      return null;
  }
};
